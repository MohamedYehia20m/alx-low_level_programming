#include "calc.h"

/**
 * op_add - calculate the sum of the two operand
 * @a: first operand
 * @b: second operand
 *
 * Description: calculate the sum of the two operand
 *
 * Return: return the sum of the two operand
 */

int op_add(int a, int b)
{
	return ((a) + (b));
}

/**
 * op_sub - calculate the sub of the two operand
 * @a: first operand
 * @b: second operand
 *
 * Description: calculate the sub of the two operand
 *
 * Return: return the sub of the two operand
 */

int op_sub(int a, int b)
{
        return ((a) - (b));
}

/**
 * op_mul - calculate the mul of the two operand
 * @a: first operand
 * @b: second operand
 *
 * Description: calculate the mul of the two operand
 *
 * Return: return the mul of the two operand
 */

int op_mul(int a, int b)
{
        return ((a) * (b));
}

/**
 * op_div - calculate the division of the two operand
 * @a: first operand
 * @b: second operand
 *
 * Description: calculate the division of the two operand
 *
 * Return: return the division of the two operand
 */

int op_div(int a, int b)
{
        return ((a) / (b));
}

/**
 * op_mod - calculate the mod of the two operand
 * @a: first operand
 * @b: second operand
 *
 * Description: calculate the mod of the two operand
 *
 * Return: return the mod of the two operand
 */

int op_mod(int a, int b)
{
        return ((a) % (b));
}
